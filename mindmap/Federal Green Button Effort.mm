    <p>
      [jat] That is correct. The NASA owned meters are producing data that must be transformed into Green Button format. This is generally accomplished through an energy management system that receives the raw meter data, may store it locally, and also may provide local facilities or energy management capabilities to the site administrator. In the case of GSA, the Schneider Electric ION EEM product provided that energy management service. The GSA activity demonstrated using the at the fence line master meter data from the Utility AND the GSA owned meters within the buildings in the same way.
